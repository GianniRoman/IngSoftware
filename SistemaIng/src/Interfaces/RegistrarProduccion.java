    public void llenarlista(){
        ConexionBD conexion = new ConexionBD();
        conexion.Conectar();
        
        try{
            conexion.setS(conexion.getConexion().createStatement());
            conexion.setRs(conexion.getS().executeQuery("SELECT * FROM lactometro"));
            while(conexion.getRs().next()){
                DatoLactometro lact = new DatoLactometro();
                lact.setCant(conexion.getRs().getFloat("cant"));
                lact.setDens(conexion.getRs().getFloat("dens"));
                lact.setVcod(conexion.getRs().getString("vcod"));
                modeloLista.addElement(lact);
            }
        }
        catch(SQLException ex){
            System.out.println(ex);
        }
    }


        ConexionBD conexion = new ConexionBD();
        conexion.Conectar();
        fecha = jDateChooser1.getDate();
        int index = 0;
        while(index<jList1.getModel().getSize()){
        DatoLactometro dato = (DatoLactometro)modeloLista.getElementAt(index);
        try{
            conexion.setS(conexion.getConexion().createStatement());
            conexion.getConexion().createStatement().execute("INSERT INTO produccion VALUES ('"+dato.getVcod()+"',"+fecha.getYear()+1900+","+fecha.getMonth()+1+","+dato.getCant()+","+dato.getDens()+")");
        }
        catch(SQLException ex){
            System.out.println(ex);
        }
        index++;
        }
